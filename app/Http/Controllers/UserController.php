    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $search = $request->input('search');
        if($search){
            $users = User::where(
                'user', "LIKE", '%'.$search.'%'
            )->orWhere(
                'email', "LIKE", '%'.$search.'%'
            )->paginate(15);
        }else{
            $users = User::paginate(15);
        }
        return view('users.index', compact('users', 'search'));
    }

